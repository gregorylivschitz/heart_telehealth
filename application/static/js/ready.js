$(document).ready(function () {
    $(".button-collapse").sideNav();
    var scroll_start = 0;
    var startchange = $('.chameleon');
    var offset = startchange.offset();
    $(document).scroll(function() {
        scroll_start = $(this).scrollTop();
        if(scroll_start > offset.top) {
            $('.chameleon').removeClass('transparent no_nav_shadow').addClass('blue-grey darken-2');
        } else {
            $('.chameleon').removeClass('blue-grey darken-2').addClass('transparent no_nav_shadow');
        }
    });
});
